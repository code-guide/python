#!/usr/bin/env python3.5

''' 条件语句 '''

score = 89

# if 语句
if score >= 60:
    print('及格')

# if else 语句
if score >= 60:
    print('及格')
else:
    print('不及格')

# if elif else 语句
if score >= 90:
    print('优')
elif score >= 80:
    print('良')
elif score >= 60:
    print('及格')
else:
    print('不及格')